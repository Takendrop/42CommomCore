void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmpa;
	int	tmpb;

	if (*b != 0)
	{
		tmpa = *a / *b;
		tmpb = *a % *b;
		*a = tmpa;
		*b = tmpb;
	}
}
